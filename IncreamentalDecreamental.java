package helloworld;

public class IncreamentalDecreamental {
    
    public static void main(String[] args){
        
       int a = 1;
        
        for(int i = 10 ; a <= i ; a++ ){
            
            System.out.println(a);
        }
        
        int b = 10;
        for(int j = 1 ; b >=j ; b--){
            
            System.out.println(b);
            
        }
        
    }
    
}
