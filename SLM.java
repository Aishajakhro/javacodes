package helloworld;

public class SLM {
    
    public static void main(String[] args){
        
        // SLM means Standard Library Method
        
        //Class Constant
        
        System.out.println("the pi value is " + Math.PI);
        System.out.println("the e value is " + Math.E);
        
        //Exponent Method
        
        System.out.println("The squareroot of 4 is " + Math.sqrt(4));
        System.out.println("The power is " + Math.pow(4, 2));
        System.out.println("The exp is " + Math.exp(2));
        System.out.println("The log is " + Math.log(52));
       
        //max , min
        
        System.out.println("The minimum Value is " + Math.min(4, 2));
        System.out.println("The maximum Value is " + Math.max(4, 2));
        
        //Trignometric
        
        System.out.println(Math.sin(90));
        System.out.println(Math.cos(90));
        System.out.println(Math.tan(90));
        System.out.println(Math.asin(90));
        System.out.println(Math.acos(90));
        System.out.println(Math.atan(90));
    }
    
}
